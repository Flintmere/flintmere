/**
 * EvidenceFigure — annotated synthetic product records that demonstrate
 * the methodology. Two side-by-side mock catalog rows for the same
 * product (Yorkshire honey, 350g): one passes the checks, one fails on
 * Identifiers + Attributes. Hairline rules + mono callouts in the page's
 * existing register.
 *
 * Why this is the right "imagery proves" move (per CLAUDE.md §canon shift
 * 2026-04-26): the prose claims "a missing or invalid identifier is the
 * most common reason a product is suppressed." Here is what that looks
 * like in actual catalog data, with the rule that fired and the score
 * impact on the right side. Type leads, evidence proves.
 *
 * Why NOT Runware/Flux for this figure: it needs perfect text rendering
 * (mono labels, GTIN strings, structured-data keys) and pixel-precise
 * hairline alignment to the design tokens. Generative photoreal will
 * garble UI text and break alignment. The atmospheric still-life
 * generated by Runware lives at the head of the Standard chapter, not
 * here.
 *
 * Pure server component, no JS. Synthetic data — no real merchant
 * implicated. Single semantic tokens (--color-ok, --color-alert) for
 * pass/fail dots so the meaning survives without colour (icon + label).
 */

interface FieldRow {
  label: string;
  pass: { value: string; status: 'ok' | 'partial' };
  fail: { value: string; status: 'alert' | 'partial' | 'ok' };
  failNote?: string;
}

