interface RecordCardProps {
  kind: 'pass' | 'fail';
  rows: { label: string; value: string; status: 'ok' | 'partial' | 'alert'; note?: string }[];
  scores: { identifiers: string; attributes: string; titles: string };
}

function RecordCard({ kind, rows, scores }: RecordCardProps) {
  const heading = kind === 'pass' ? 'How a strong record looks' : 'How a record fails the checks';
  const statusEyebrow = kind === 'pass' ? 'Passes' : 'Fails';
  const eyebrowColor = kind === 'pass' ? 'var(--color-ok)' : 'var(--color-alert)';

  return (
    <div
      className="bg-[color:var(--color-paper)]"
      style={{
        padding: 'clamp(20px, 2.5vw, 32px)',
      }}
    >
      <div className="flex items-center gap-3 mb-2">
        <span
          aria-hidden="true"
          style={{
            width: 8,
            height: 8,
            borderRadius: '50%',
            background: eyebrowColor,
            display: 'inline-block',
          }}
        />
        <p
          className="font-mono uppercase"
          style={{
            fontSize: 11,
            letterSpacing: '0.16em',
            color: eyebrowColor,
          }}
        >
          {statusEyebrow}
        </p>
      </div>

      <p
        className="font-medium text-[color:var(--color-ink)]"
        style={{
          fontSize: 'clamp(16px, 1.4vw, 19px)',
          letterSpacing: '-0.01em',
          marginBottom: 'clamp(20px, 2.4vw, 28px)',
        }}
      >
        {heading}
      </p>

      <dl className="flex flex-col gap-0">
        {rows.map((row, i) => (
          <div
            key={row.label}
            className="grid grid-cols-[110px_1fr] gap-3 py-3"
            style={{
              borderTop: i === 0 ? '1px solid var(--color-line)' : '1px solid var(--color-line-soft)',
            }}
          >
            <dt
              className="font-mono uppercase text-[color:var(--color-mute-2)]"
              style={{ fontSize: 10, letterSpacing: '0.14em', alignSelf: 'start', paddingTop: 2 }}
            >
              {row.label}
            </dt>
            <dd>
              <p
                className="font-mono text-[color:var(--color-ink)] break-words"
                style={{
                  fontSize: 13,
                  lineHeight: 1.45,
                }}
              >
                {row.value}
              </p>
              {row.note ? (
                <p
                  className="text-[color:var(--color-alert)] flex items-start gap-2"
                  style={{
                    fontSize: 11,
                    lineHeight: 1.4,
                    marginTop: 4,
                    fontStyle: 'italic',
                  }}
                >
                  <span aria-hidden="true">&rarr;</span>
                  <span>{row.note}</span>
                </p>
              ) : null}
            </dd>
          </div>
        ))}
      </dl>

      <div
        className="grid grid-cols-3 gap-4"
        style={{
          marginTop: 'clamp(24px, 3vw, 32px)',
          paddingTop: 'clamp(20px, 2.4vw, 24px)',
          borderTop: '1px solid var(--color-line)',
        }}
      >
        <ScoreCell label="Identifiers" value={scores.identifiers} kind={kind} />
        <ScoreCell label="Attributes" value={scores.attributes} kind={kind} />
        <ScoreCell label="Titles" value={scores.titles} kind={kind} />
      </div>
    </div>
  );
}

function ScoreCell({ label, value, kind }: { label: string; value: string; kind: 'pass' | 'fail' }) {
  const valueColor = kind === 'pass' ? 'var(--color-ok)' : 'var(--color-alert)';
  return (
    <div>
      <p
        className="font-mono uppercase text-[color:var(--color-mute-2)]"
        style={{ fontSize: 10, letterSpacing: '0.14em', marginBottom: 4 }}
      >
        {label}
      </p>
      <p
        className="font-mono font-medium"
        style={{
          fontSize: 'clamp(18px, 1.6vw, 22px)',
          letterSpacing: '-0.02em',
          fontVariantNumeric: 'tabular-nums',
          color: valueColor,
        }}
      >
        {value}
      </p>
    </div>
  );
}
